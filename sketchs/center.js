p=[]
setup=_=>{
r=random
l=createCanvas(w=192,h=157)
l.parent('CenterColorssketch')
strokeWeight(20)
for(x=99;x--;){
y=r(6.28)
z=2*y
if(y>z){
s=y
y=z
z=s}
p.push([r(w),y,z,r(w),r(w),r(w),0])
}
}
draw=_=>{
noFill()
for(x=99;x--;){
t=p[x]
t[6]+=.01
stroke(t[3],t[4],t[5])
arc(w/2,h/2,t[0],t[0],t[1],t[2]+t[6])
}
}
